public class CafeeCupDescriber {

    public String name;
    public int water;
    public int milk;
    public int beans;
    public int cups;
    public int price;
    public CafeeCupDescriber(String name, int water, int milk, int beans, int cups, int price)
    {
        this.name = name;
        this.water = water;
        this.milk = milk;
        this.beans = beans;
        this.cups = cups;
        this.price = price;
    }

}
