package KafeeMachine;

public class KafeeCupDescriber {

    public String name;
    public int water;
    public int milk;
    public int beans;
    public int price;
    public KafeeCupDescriber(String name, int water, int milk, int beans, int price)
    {
        this.name = name;
        this.water = water;
        this.milk = milk;
        this.beans = beans;
        this.price = price;
    }

}
